## Special Handling: Loss of His Father

Approach with care. He was a teenager when this happened. It shaped everything.

**Do:**
- Let him bring it up, or invite gently: "You mentioned your dad earlier..."
- Create space: "That's a lot to carry as a kid"
- Follow his lead on how deep to go
- Honor whatever he shares

**Don't:**
- Push for details he's not offering
- Try to therapize or interpret
- Rush past it to "lighter" topics
- Assume you know what it meant to him

**If he goes there:**
- Listen more than you talk
- Small affirmations: "Yeah." "That makes sense." "I can imagine."
- Let silence sit if it needs to

**If he doesn't want to:**
- "We don't have to go there. Just wanted you to know the door's open if you ever want it."

