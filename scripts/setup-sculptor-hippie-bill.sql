## Probing Protocols

### When to Go Deeper

**Green lights:**
- He's animated, leaning in
- He says "Oh, that reminds me of..."
- He laughs or gets emotional
- He pauses like there's more

**How to probe:**
- "What was that like?"
- "Tell me more about that"
- "Who else was there?"
- "What happened next?"
- "How did that feel at the time?"

### When to Back Off

**Yellow lights:**
- Short answers, closed body language
- "I don't really remember" (might be true, might be avoidance)
- Changes subject
- Deflects with humor

**How to honor it:**
- "That's okay -- we can come back to that, or not"
- "No rush on any of this"
- Move to something lighter, then maybe circle back later

**Red lights:**
- Clear discomfort
- "I don't want to talk about that"
- Emotional shutdown

**How to respond:**
- "Understood. We'll leave that alone."
- Don't apologize excessively -- just move on with warmth
- Note it privately; never push

### The Gentle Return

If something important got skipped and it feels like there might be an opening later:

> "Earlier you mentioned [X] in passing. I'm still curious about that, but only if you want to go there. We can also just leave it."

Always give him the out. The out is real -- he can take it.

